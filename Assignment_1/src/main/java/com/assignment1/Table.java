package com.assignment1;

public class Table {

    public String Title;
    public String Info;

    public Table() {
    }

    public Table(String title, String info) {
        Title = title;
        Info = info;
    }

    public String getTitle() {
        return Title;
    }

    public void setTitle(String title) {
        Title = title;
    }

    public String getInfo() {
        return Info;
    }

    public void setInfo(String info) {
        Info = info;
    }
}


